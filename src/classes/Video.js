class Video {
    constructor(title, imgUrl, description, author, publicationDate, viewCount, videoId) {
        this.title = title;
        this.imgUrl = imgUrl;
        this.description = description;
        this.author = author;
        this.publicationDate = publicationDate;
        this.viewCount = viewCount;
        this.videoId = videoId;
    }
}

export default Video;
